    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        BufferedImage image = s.getLastImage();
        Image originalImage = image;
        int imgHeight = image.getHeight(null);
        int imgWidth = image.getWidth(null);

        double heightRatio = ((double) imgHeight / (double) jPanel1.getHeight());
        double widthRatio = ((double) imgWidth / (double) jPanel1.getWidth());

        BufferedImage toDraw;

        if (heightRatio > 1 || widthRatio > 1) {
            if (heightRatio > widthRatio) {
                toDraw = new BufferedImage((int) Math.round(imgWidth / heightRatio), jPanel1.getHeight(), BufferedImage.TYPE_INT_RGB);
            } else {
                toDraw = new BufferedImage(jPanel1.getWidth(), (int) Math.round(imgHeight / widthRatio), BufferedImage.TYPE_INT_RGB);
            }
            Graphics2D g = toDraw.createGraphics();
            g.drawImage(originalImage, 0, 0, toDraw.getWidth(), toDraw.getHeight(), null);
            g.dispose();
        } else {
            toDraw = image;
        }

        jLabel1.setIcon(new ImageIcon(toDraw));
    }//GEN-LAST:event_jButton4ActionPerformed

